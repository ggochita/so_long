#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	while (s && s[i])
	{
		if (f != NULL)
			(*f)(i, &s[i]);
		i++;
	}
}
